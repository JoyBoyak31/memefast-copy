  // Generate mock candlestick data for a token
  export const generateMockCandlesticks = (tokenAddress, timeframe = 5, limit = 100) => {
    const token = mockTokens.find(t => t.address === tokenAddress);
    if (!token) return [];
  
    const now = Date.now();
    const candlesticks = [];
    const basePrice = token.price;
    const volatility = basePrice * 0.03; // 3% volatility
  
    // Create realistic price movements with some trends
    let currentPrice = basePrice;
    for (let i = limit - 1; i >= 0; i--) {
      const time = now - (i * timeframe * 60 * 1000);
      
      // Add some randomness with mild trends
      const trend = Math.sin(i / 10) * 0.01; // Gentle sine wave trend
      const random = (Math.random() - 0.5) * 0.02; // Random factor
      
      // Price change from previous candle 
      const change = (trend + random) * basePrice;
      currentPrice = Math.max(0.000001, currentPrice + change); // Prevent negative prices
      
      const open = currentPrice;
      const close = currentPrice * (1 + (Math.random() * 0.02 - 0.01));
      const high = Math.max(open, close) * (1 + Math.random() * 0.01);
      const low = Math.min(open, close) * (1 - Math.random() * 0.01);
      
      // Calculate volume based on price volatility
      const priceChange = Math.abs(close - open) / open;
      const volume = token.volume24h * (timeframe / 1440) * (1 + priceChange * 10);
  
      candlesticks.push({
        time,
        open,
        high,
        low,
        close,
        volume
      });
    }
  
    return candlesticks;
  };
  
